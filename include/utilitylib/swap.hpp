#ifndef _EXNG2_UTILITYLIB_SWAP_HPP_
# define _EXNG2_UTILITYLIB_SWAP_HPP_

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  If you don't have STL, this provides std::swap behaviour
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template<typename T>
inline void swap(T& a, T& b)
{
  T tmp(a);
  a=b;
  b=tmp;
}

#endif
